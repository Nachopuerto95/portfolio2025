export function applyPalette(paletteId) {
	const palette = palettes.find((p) => p.id === paletteId) || palettes[0];
	const root = document.documentElement;
	Object.entries(palette.vars).forEach(([k, v]) => {
		root.style.setProperty(k, v);
	});
	return palette.id;
}
